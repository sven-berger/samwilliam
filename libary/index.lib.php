<section class="mb-10 content" id="Rails">
  <h3>Rails, die Alternative zu PHP?</h3>
  <p>Ich werde Ruby on Rails lernen, weil es die oben genannten Vorteile bietet, viele Möglichkeiten von Haus aus mit sich bringt und dadurch den Programmieraufwand deutlich reduziert.</p>
  <p>Zusätzlich ist Rails für mich interessant, da mein Praktikumsbetrieb von PHP auf Rails gewechselt ist und ich dadurch mit der Arbeitsweise direkt vertraut werden kann.</p>
  <p>Ein weiterer Pluspunkt: Mit Stimulus, das von den gleichen Entwicklern stammt, sowie der guten Zusammenarbeit mit React ergeben sich spannende Möglichkeiten für zukünftige Projekte.</p>
</section>

<section class="mb-10 content" id="React">
  <h3>Das Webmonster React</h3>
  <p>Ich möchte React erlernen, weil es im modernen Web nahezu allgegenwärtig ist – egal ob Webseiten oder Webapps.</p>
  <p>Als JavaScript-Bibliothek bietet es mächtige Funktionen und erlaubt sogar die Entwicklung von Anwendungen für Smart-Geräte.</p>
  <p>Besonders interessant finde ich, wie nahtlos React mit Backends wie Rails oder PHP zusammenarbeitet.</p>
  <p>Auch mein Praktikumsbetrieb setzt auf React: Für kleinere Projekte wird dort Stimulus genutzt, bei größeren Projekten hingegen React – und genau diesen Praxisbezug möchte ich für mich aufbauen.</p>
</section>

